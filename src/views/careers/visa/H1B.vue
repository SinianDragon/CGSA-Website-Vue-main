    <h3 id="h1b-intro">H-1B介绍</h3>
    <ul>
      <li>
        H-1B是美国签证的一种，指的是特殊专业人员/临时工作签证Specialty Occupations/ Temporary Worker Visas (H-1B)。 H-1B签证是美国最主要的工作签证类别，发放给美国公司雇佣的外国籍有专业技能的员工，属于非移民签证的一种。 H-1B成立的意图适用于解决是美国公司急需但在其国内又招不到的人才。因此，它对申请人的学历有一定要求，必须具备学士或以上学位。
      </li>
      <li>
        持有H-1B签证者可以在美国工作三年，然后可以再延长三年，6年期满后如果签证持有者的身份还没有转化，就必须离开美国。 大多数H-1B的持有者都希望能在这段时间内完成绿卡申请，如果失败的话，持有者需离开美国至少365天，然后才可以再次申请H-1B 签证。根据最新政策规定，当H-1B持有人失去工作后，可以获得长达60天的宽限期（Grace Period），在此以期间以合法留美， 计划下一步。
      </li>
